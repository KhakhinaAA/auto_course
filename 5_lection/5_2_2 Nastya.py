# 袩芯胁褌芯褉褟褕泻懈
# 袧邪锌懈褕懈褌械 褎褍薪泻褑懈褞 repeats, 泻芯褌芯褉邪褟 锌褉懈薪懈屑邪械褌 薪邪 胁褏芯写 褋褌褉芯泻褍 our_str,
# 褋屑芯褌褉懈褌, 褋泻芯谢褜泻芯 褉邪蟹 泻邪卸写褘泄 褋懈屑胁芯谢 褍卸械 胁褋褌褉械褔邪谢褋褟, 懈 写芯斜邪胁谢褟械褌 泻芯谢懈褔械褋褌胁芯 泻 褋懈屑胁芯谢邪屑 褋 锌芯屑芯褖褜褞 锌芯褋褌褎懈泻褋邪 褎芯褉屑邪褌邪 _n.
# 袙芯蟹胁褉邪褖邪械褌褋褟 薪芯胁邪褟 褋褌褉芯泻邪
# 袧邪锌褉懈屑械褉 (袙胁芯写 --> 袙褘胁芯写):
# 'letter' --> l_1e_1t_1t_2e_2_r_1

def repeats(our_str):
    """
    C屑芯褌褉懈褌, 褋泻芯谢褜泻芯 褉邪蟹 泻邪卸写褘泄 褋懈屑胁芯谢 褍卸械 胁褋褌褉械褔邪谢褋褟,
     懈 写芯斜邪胁谢褟械褌 泻芯谢懈褔械褋褌胁芯 泻 褋懈屑胁芯谢邪屑 褋 锌芯屑芯褖褜褞 锌芯褋褌褎懈泻褋邪 褎芯褉屑邪褌邪 _n

     :param our_str: str
    """
    # 小锌懈褋芯泻 写谢褟 褏褉邪薪械薪懈褟 锌褉芯屑械卸褍褌芯褔薪褘褏 褉械蟹褍谢褜褌邪褌芯胁
    result = []
    # 小谢芯胁邪褉褜 写谢褟 芯褌褋谢械卸懈胁邪薪懈褟 泻芯谢懈褔械褋褌胁邪 锌芯胁褌芯褉械薪懈泄 褋懈屑胁芯谢芯胁
    counts = {}
    for char in our_str:
        if char not in counts:
            counts[char] = 1
        else:
            counts[char] += 1
        # 袣邪卸写褘泄 褉邪蟹 锌芯褋谢械 芯斜薪芯胁谢械薪懈褟 褋褔褢褌褔懈泻邪 褎芯褉屑懈褉褍械屑 薪芯胁褍褞 褋褌褉芯泻褍 褎芯褉屑邪褌邪 "邪_5"
        result.append(f"{char}_{counts[char]}")
    return ''.join(result)

# 袧懈卸械 袧袠效袝袚袨 袧袝 袧袗袛袨 袠袟袦袝袧携孝鞋


